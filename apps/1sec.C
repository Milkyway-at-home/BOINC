// burn up to one CPU second

#include "config.h"
#include <cstdio>
#include <ctime>

int main() {
    int now = time(0), i;
    double x=0;

    while (1) {
        for (i=0; i<1000000; i++) x += 1;
        if (time(0) != now) break;
    }

    FILE* f = fopen("out", "w");
    fprintf(f, "done\n");
    fclose(f);
}

