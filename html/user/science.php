    <a href="http://rpi.edu/~vickej2/boincPics/pplot1.JPG"><img class="align-center" src="http://rpi.edu/~vickej2/boincPics/pplot1.JPG" width="600" /></a> <br />
    <a href="http://rpi.edu/~vickej2/boincPics/pplot2.JPG"><img class="align-center" src="http://rpi.edu/~vickej2/boincPics/pplot2.JPG" width="600" /></a>
    </div>
    <p>Here is the corresponding plot in a plane perpendicular to the above. Imagine now that you had the prior plot on a piece of paper and you tilted it until all you see is a line. That line (which represents a plane) is signified by the middle line in this plot. So putting these two plots together would yield a 3D interpretation of the found points and directions of the streams.</p>
    <p>So from each run we want to obtain 3 good indicators: First, the separation plot should leave a near uniform background-- if there's still overdensities in the output, we are not getting an accurate picture of the 2 spheroid parameters. Second, the vectors in the plane should be cohesive-- we want the stream to flow rather than zig zag through space as it were. Third, we want the vectors in the perpendicular plane to be close to parallel to the plane-- again, we want it to flow, not zig zag.</p>
	<p>We did all that, Nate wrote his Thesis on it. So what are we doing now? Basically at this point we want to refine our results and get them to be more accurate. To do this we have stitched all the SDSS data together and taken wedges out that are perpendicular to the stream-- the general idea is that a perpendicular cross section is much easier to decipher than a skewed one-- thus our error measurements will be smaller and the likelihoods will be higher. I have just now begun runs on BOINC using this new geometry (all the recent *_sgr_* runs), although I have been working with it since the beginning of last summer on the 88 processor WCL grid here at RPI. For reference, it took me about a week per run on the MPI grid-- now I am getting about 5 runs per day on BOINC, it's amazing.</p>
    <img class="align-center" src="http://rpi.edu/~vickej2/boincPics/diagram.JPG" width="400" />
    <p>Here is a simple diagram illustrating the idea-- the blue line is the stream in question and the black lines represent wedges of data. The left represents SDSS stripes and the right represents the improved perpendicular SGR stripes.</p>
    <div class="align-left">
