        <a href="http://rpi.edu/~vickej2/boincPics/John.JPG"><img src="http://rpi.edu/~vickej2/boincPics/John.JPG" width="250" /></a>
     </div>
    <p>Here is a juxtaposition of one of Nate's wedges (left, sdss stripe 13) and one of mine (right, sgr stripe 35) in the same area of 3d space-- notice how his stream stretches almost all the way across the stripe because it is tilted relative to the stripe while mine is nice and compact. This translates to smaller errors in our reported findings.</p>
	<p>So our basic BOINC goal is to now remap the whole stream so that it is not only cohesive like Nate's findings, but also more accurate than his findings. After that (a couple of months from now, optimistically) I will attempt to map any other streams we can find in the data and remove them as well. Then my fellow student, Matt Newby, can get into the meat of his project which is modeling the 2 spheroid parameters throughout the entire sky (imagine putting 30 uniform wedges into BOINC at once and searching for 0 streams).				</p>
    <p>These are both tremendous topics in modern astrophysics-- first of all, the location and direction of the Sagittarius stream is still somewhat debated. Some people, like Nate, believe that the stream passes by us. Others think that the stream crashes down on top of the Sun. And the spheroid has yet to be accurately modeled. Such a model would make galactic simulations much easier to create as they would require less unknown variables in their simulations; and it could also provide valuable clues to the dark matter problem.</p>
