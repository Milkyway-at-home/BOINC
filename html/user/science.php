    <h1>Science</h1>
    <hr />
    <h2>Astrophysics Overview</h2>
    <div class="align-right">
		<a href="http://rpi.edu/~vickej2/boincPics/sdssSkyCoverage.gif"><img height="250" width="250" src="http://rpi.edu/~vickej2/boincPics/sdssSkyCoverage.gif" /></a>
        <a href="http://rpi.edu/~vickej2/boincPics/SegueSkyCoverage.gif"><img height="250" width="250" src="http://rpi.edu/~vickej2/boincPics/SegueSkyCoverage.gif" /></a>
    </div>
    <p>In the beginning there were stars. Then there were people, and every night these people would look up into the skies and they would notice how intriguingly complex a place the heavens really were. Wandering planets travel throughout an ever turning mosaic of mythology and mysticism. The sky was one of the biggest mysteries of the ancient world-- what do these pictures mean?</p>
    <p>Our project begins with the <a href="http://www.sdss.org">Sloan Digital Sky Survey</a>, an ambitious project whose goal is to map out as large a portion of the sky as possible. To this date the SDSS has mapped about a quarter of the sky, including over 300 million objects.</p>
    <p>These are (l, b) (l and b are galactic longitude and latitude respectively-- the equator being the galactic plane) plots of the amount of sky covered by the Sloan Digital Sky Survey (SDSS1 on the left and SDSS2 on the right).</p>
	<p>But what really is a list of a couple million points in 3D space other than a massive problem to tackle? Sure, we can plot all these points together and get a gorgeous map of the sky, but once again-- what do these pictures mean?</p>
	<p>That's where research astrophysicists come into play. One of the hot spots in galactic astronomy (astronomy relating to just the Milky Way) at the moment is stellar stream mapping. The general idea is that the Milky Way Galaxy actually has a couple of smaller galaxies mixed in with it, probably from galactic collisions (<a href="http://rpi.edu/~vickej2/boincPics/surf.mpg">click here</a> for a simulation of how a galactic collision turns a galaxy into a stream-- simulation by Kathryn V. Johnston at Columbia University) beginning sometime in ancient history and continuing to this day (don't worry, it is very seldom that actual material like stars or planets collide-- there is so much empty space that it is highly improbable). The Sagittarius Dwarf Galaxy is one of the closer galaxies residing in our own and it is our particular area of interest.</p>
    <div class="align-left">
    	<a href="http://rpi.edu/~vickej2/boincPics/sdssSep.jpg"><img  width="400" src="http://rpi.edu/~vickej2/boincPics/sdssSep.jpg" /></a>
    </div>
    <p>In general an astrophysics problem revolves around creating a model on a computer system that will replicate what we see in the sky: if a model matches exactly then we can leapfrog off the information that model reveals to work on a bigger, more involved problem. Currently, the MW@Home BOINC application is made to model plates of stars. We input a 2.5 degree cross section of data (the shape is commonly called a wedge, or stripe) and the program attempts to create a new, uniformly dense wedge of stars from the input wedge by removing a stream(s) of data. The streams it removes are necessarily cylindrical and their stellar density falls off in a Guassian manner (denser in the middle, sparser at the edges).</p>
    <p>The image on the left is a sample separation, the upper right circle is the input wedge-- it is a density map of that cross section of sky with darker areas being more stellarly dense and lighter sections being less dense. The lower circle is the removed cylinder of stars and the upper left circle is the hopefully uniform wedge of stars that remains after removing the stars in the lower circle. For reference we (the Solar System) are at the exact center of these plots (since all of our data was gathered here on Earth).</p>
	<p>Each stream removed possesses 6 parameters: weight (% of stars in the stream), mu (a measure of angular position in the stripe, given by the ticks on the circumference of the above plots), r ( a measure of distance, given by the radial ticks above), phi (one 3D angle indicating direction of the removed cylinder), theta (the second required angle), and sigma (a measure of width). And each wedge background possesses 2 parameters: q (a measure of the flatness of the spheroid) and r0 (a measure of the radius of the spheroid core). So every run has 2+6n parameters, where n is the number of streams being modeled.</p>
    <div class="align-right">
    	<a href="http://rpi.edu/~vickej2/boincPics/prettyPic_all_noArrow.jpg"><img src="http://rpi.edu/~vickej2/boincPics/prettyPic_all_noArrow.jpg" width="400" /></a>
    </div>
	<p>Here is a top-down view of one possible model for the Sagittarius Dwarf Stream. The middle galaxy represents the Milky Way with the sun being the green dot within the disk. The blue stars are the general areas of the Sagittarius dwarf that we study. This is in the plane of the Sagittarius dwarf stream, so imagine we are looking down on top of a semi-flat structure-- click <a href="http://rpi.edu/~vickej2/boincPics/sgr.flyaround.mpg">here</a> for a 3D model produced by David Law at the University of Virginia.</p>
	<p>What we want to do is end up with as many data points as possible from BOINC-- we can use mu and r to plot the location in space and the angles phi and theta to plot the direction of the stream. What we end up with is a picture similar to the above.</p>
    <p>Below is a plot of all of the data point positions and directions found by Nathan Cole-- it is exactly the same as the picture just before it, just less artsy.</p>
